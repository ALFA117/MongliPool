#![no_std]

use soroban_sdk::{contract, contractimpl, contracttype, Bytes, BytesN, Env, Vec};
use soroban_sdk::crypto::bn254::{Bn254G1Affine, Bn254G2Affine, Fr};

// VK serialization layout (compatible with snarkjs exportVerificationKey output):
//   [0   .. 64 ] alpha_g1  : G1 (64 bytes)
//   [64  .. 192] beta_g2   : G2 (128 bytes)
//   [192 .. 320] gamma_g2  : G2 (128 bytes)
//   [320 .. 448] delta_g2  : G2 (128 bytes)
//   [448 .. 452] num_ic    : u32 big-endian  (= num_public_inputs + 1)
//   [452 ..     ] IC[0..num_ic] : G1, 64 bytes each
//
// Proof serialization layout (compatible with snarkjs groth16 proof export):
//   [0   .. 64 ] A : G1 (64 bytes)
//   [64  .. 192] B : G2 (128 bytes)
//   [192 .. 256] C : G1 (64 bytes)
//   Total: 256 bytes
//
// G1 encoding: be(X) || be(Y), each 32 bytes (Ethereum-compatible uncompressed)
// G2 encoding: be(X.c1) || be(X.c0) || be(Y.c1) || be(Y.c0), imaginary-first (EIP-197)

#[contracttype]
pub enum DataKey {
    Vk,
    NumInputs,
}

#[contract]
pub struct Groth16Verifier;

#[contractimpl]
impl Groth16Verifier {
    /// Store the Groth16 verification key. Must be called once after deployment.
    /// vk_bytes must follow the layout documented at the top of this file.
    pub fn initialize(env: Env, vk_bytes: Bytes) {
        if env.storage().instance().has(&DataKey::Vk) {
            panic!("already initialized");
        }
        // Need at least the 4 fixed EC points (448 bytes) + 4-byte count + 1 IC point (64 bytes)
        if vk_bytes.len() < 452 + 64 {
            panic!("vk too short");
        }
        let num_ic = parse_u32(&vk_bytes, 448);
        if num_ic == 0 {
            panic!("num_ic must be >= 1");
        }
        let min_len = 452u32
            .checked_add(num_ic.checked_mul(64).unwrap())
            .unwrap();
        if vk_bytes.len() < min_len {
            panic!("vk truncated");
        }
        env.storage().instance().set(&DataKey::Vk, &vk_bytes);
        // num_inputs = num_ic - 1 (IC[0] is the constant term, not tied to an input)
        env.storage().instance().set(&DataKey::NumInputs, &(num_ic - 1));
    }

    /// Verify a Groth16 proof against the stored verification key.
    ///
    /// Performs the full BN254 multi-pairing check:
    ///   e(-A, B) * e(alpha, beta) * e(vk_x, gamma) * e(C, delta) = 1
    /// where vk_x = IC[0] + sum(IC[i+1] * inputs[i]) for i in 0..num_inputs.
    pub fn verify(
        env: Env,
        proof_bytes: Bytes,
        public_inputs: Vec<BytesN<32>>,
    ) -> bool {
        // Proof must be exactly 256 bytes: A(64) + B(128) + C(64)
        if proof_bytes.len() != 256 {
            return false;
        }

        // Load VK; return false if not yet initialized
        let vk_bytes: Bytes = match env.storage().instance().get(&DataKey::Vk) {
            Some(v) => v,
            None => return false,
        };

        // Validate input count against the stored VK
        let num_inputs: u32 = env
            .storage()
            .instance()
            .get(&DataKey::NumInputs)
            .unwrap_or(0);
        if public_inputs.len() != num_inputs {
            return false;
        }

        let bn254 = env.crypto().bn254();

        // --- Parse proof ---
        let a = extract_g1(&proof_bytes, 0);
        let b = extract_g2(&proof_bytes, 64);
        let c = extract_g1(&proof_bytes, 192);

        // --- Parse VK ---
        let alpha = extract_g1(&vk_bytes, 0);
        let beta  = extract_g2(&vk_bytes, 64);
        let gamma = extract_g2(&vk_bytes, 192);
        let delta = extract_g2(&vk_bytes, 320);

        // --- Compute vk_x = IC[0] + sum(IC[i+1] * inputs[i]) ---
        const IC_BASE: u32 = 452;
        let mut vk_x = extract_g1(&vk_bytes, IC_BASE);
        for i in 0..num_inputs {
            let ic_point = extract_g1(&vk_bytes, IC_BASE + 64 + i * 64);
            let scalar = Fr::from_bytes(public_inputs.get(i).unwrap());
            vk_x = bn254.g1_add(&vk_x, &bn254.g1_mul(&ic_point, &scalar));
        }

        // --- Groth16 multi-pairing check ---
        // e(-A, B) * e(alpha, beta) * e(vk_x, gamma) * e(C, delta) == 1 in GT
        let neg_a = -a;

        let mut p1: Vec<Bn254G1Affine> = Vec::new(&env);
        p1.push_back(neg_a);
        p1.push_back(alpha);
        p1.push_back(vk_x);
        p1.push_back(c);

        let mut p2: Vec<Bn254G2Affine> = Vec::new(&env);
        p2.push_back(b);
        p2.push_back(beta);
        p2.push_back(gamma);
        p2.push_back(delta);

        bn254.pairing_check(p1, p2)
    }
}

// ---------------------------------------------------------------------------
// Internal helpers (not exposed as contract entry points)
// ---------------------------------------------------------------------------

fn parse_u32(bytes: &Bytes, offset: u32) -> u32 {
    let b0 = bytes.get(offset).unwrap_or(0) as u32;
    let b1 = bytes.get(offset + 1).unwrap_or(0) as u32;
    let b2 = bytes.get(offset + 2).unwrap_or(0) as u32;
    let b3 = bytes.get(offset + 3).unwrap_or(0) as u32;
    (b0 << 24) | (b1 << 16) | (b2 << 8) | b3
}

fn extract_g1(bytes: &Bytes, offset: u32) -> Bn254G1Affine {
    Bn254G1Affine::from_bytes(
        BytesN::<64>::try_from(bytes.slice(offset..offset + 64)).unwrap(),
    )
}

fn extract_g2(bytes: &Bytes, offset: u32) -> Bn254G2Affine {
    Bn254G2Affine::from_bytes(
        BytesN::<128>::try_from(bytes.slice(offset..offset + 128)).unwrap(),
    )
}

