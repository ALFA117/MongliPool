    #[test]
    fn test_known_pairing_check() {
        let env = Env::default();
        let bn254 = env.crypto().bn254();

        let g1     = Bn254G1Affine::from_bytes(BytesN::<64>::from_array(&env, &G1_GEN));
        let neg_g1 = Bn254G1Affine::from_bytes(BytesN::<64>::from_array(&env, &NEG_G1));
        let g2     = Bn254G2Affine::from_bytes(BytesN::<128>::from_array(&env, &G2_GEN));

        let mut p1: Vec<Bn254G1Affine> = Vec::new(&env);
        p1.push_back(g1);
        p1.push_back(neg_g1);

        let mut p2: Vec<Bn254G2Affine> = Vec::new(&env);
        p2.push_back(g2.clone());
        p2.push_back(g2);

        // e(G1, G2) * e(-G1, G2) = e(G1, G2) * e(G1, G2)^-1 = 1
        assert!(bn254.pairing_check(p1, p2), "EIP-197 canonical pairing check failed");
    }

