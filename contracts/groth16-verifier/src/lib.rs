    /// Builds a minimal VK for a 0-input circuit (num_ic = 1, IC = [ic0]).
    /// Total = 64 + 128 + 128 + 128 + 4 + 64 = 516 bytes.
    fn make_vk_0_input(env: &Env, alpha: &[u8; 64], ic0: &[u8; 64]) -> Bytes {
        let mut buf = [0u8; 516];
        buf[0..64].copy_from_slice(alpha);       // alpha_g1
        buf[64..192].copy_from_slice(&G2_GEN);   // beta_g2
        buf[192..320].copy_from_slice(&G2_GEN);  // gamma_g2
        buf[320..448].copy_from_slice(&G2_GEN);  // delta_g2
        buf[448..452].copy_from_slice(&[0, 0, 0, 1]); // num_ic = 1
        buf[452..516].copy_from_slice(ic0);      // IC[0]
        Bytes::from_slice(env, &buf)
    }

    /// Builds a proof from A (G1), B (G2), C (G1). Total = 256 bytes.
    fn make_proof(env: &Env, a: &[u8; 64], b: &[u8; 128], c: &[u8; 64]) -> Bytes {
        let mut buf = [0u8; 256];
        buf[0..64].copy_from_slice(a);
        buf[64..192].copy_from_slice(b);
        buf[192..256].copy_from_slice(c);
        Bytes::from_slice(env, &buf)
    }

